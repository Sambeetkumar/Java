public class Bool {
    public static void main(String[] args) {
       int num1 = 82;
       boolean isnum;
       if(num1 > 50)
        isnum = true;
       else
           isnum = false;
       if(!isnum)
       {
           System.out.println("num1 is less than 50!!");
       }
       else
       {
           System.out.println("num1 is greater than 50!!");
       }
    }
}
